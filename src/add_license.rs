//! `auditah add-license` — scaffold a license grid (`.toml`) in `LICENSES/`.

use std::fmt::Write;
use std::path::{Path, PathBuf};

use error_stack::{Report, ResultExt};
use wherror::Error;

use crate::model::terms::LicenseTerms;
use crate::services::Services;

use crate::well_known::{self, ResolveResult};
use crate::AppError;

/// Error writing a license template.
#[derive(Debug, Error)]
#[error(debug)]
pub struct AddLicenseError;

/// Compute the on-disk path for a license grid: `<root>/LICENSES/<id>.toml`.
#[must_use]
pub fn license_grid_path(root: &Path, id: &str) -> PathBuf {
    root.join("LICENSES").join(format!("{id}.toml"))
}

/// Prefix a bare name with `LicenseRef-` if it isn't already a `LicenseRef-*` id.
///
/// `Foo` → `LicenseRef-Foo`; `LicenseRef-Foo` → `LicenseRef-Foo` (idempotent).
#[must_use]
pub fn license_ref_id(name: &str) -> String {
    if name.starts_with("LicenseRef-") {
        name.to_string()
    } else {
        format!("LicenseRef-{name}")
    }
}

/// Render a fully-commented license grid template for the given id.
///
/// Every `[terms]` field gets a `#` comment explaining what it means, and the
/// header explains the `id` ↔ `LICENSES/<id>.txt` relationship so the user knows
/// they must also drop the legal text alongside this grid. Defaults are the
/// `default_fail()` baseline (maximally restrictive + `manual_review = true`):
/// the grid FAILs audit until the user fills in the real terms AND acknowledges
#[must_use]
#[allow(clippy::unwrap_used)] // writeln! to a String is infallible
pub fn render_license_template(id: &str) -> String {
    let terms = LicenseTerms::default_fail();
    let mut s = String::new();

    write_header(&mut s, id);
    write_id_fields(&mut s, id);
    write_terms(&mut s, &terms);

    s
}

/// Header: explain the `id` + `LicenseRef-` + LICENSES/<id>.txt relationship.
#[allow(clippy::unwrap_used)] // writeln! to a String is infallible
fn write_header(s: &mut String, id: &str) {
    writeln!(
        s,
        "# License definition generated by `auditah add-license`."
    )
    .unwrap();
    writeln!(s, "#").unwrap();
    writeln!(
        s,
        "# The `id` is the canonical license identifier. For custom licenses it uses the"
    )
    .unwrap();
    writeln!(
        s,
        "# `LicenseRef-<Name>` form. auditah resolves a license by matching this `id`"
    )
    .unwrap();
    writeln!(
        s,
        "# against the `license` field in an asset's attribution sidecar/manifest."
    )
    .unwrap();
    writeln!(s, "#").unwrap();
    writeln!(
        s,
        "# In addition to this grid (`.toml`), the FULL LEGAL TEXT must live in a sibling"
    )
    .unwrap();
    writeln!(
        s,
        "# file named `<id>.txt` in the same `LICENSES/` directory. audit FAILs until"
    )
    .unwrap();
    writeln!(
        s,
        "# that file exists - so after editing this grid, create:"
    )
    .unwrap();
    writeln!(s, "#").unwrap();
    writeln!(s, "#     LICENSES/{id}.txt").unwrap();
    writeln!(s, "#").unwrap();
    writeln!(
        s,
        "# with the complete license text. This grid FAILs audit until you fill in the real `[terms]` below AND add the id to `manual_review_acknowledged`."
    )
    .unwrap();
    writeln!(s).unwrap();
}

/// id / name / url fields.
#[allow(clippy::unwrap_used)] // writeln! to a String is infallible
fn write_id_fields(s: &mut String, id: &str) {
    writeln!(s, "id = \"{id}\"").unwrap();
    writeln!(s, "name = \"TODO: human-readable license name\"").unwrap();
    writeln!(s, "url = \"TODO: canonical URL of the license\"").unwrap();
    writeln!(s).unwrap();
}

/// `[terms]` table with a `#` comment above each field.
#[allow(clippy::unwrap_used)] // writeln! to a String is infallible
fn write_terms(s: &mut String, terms: &LicenseTerms) {
    writeln!(s, "[terms]").unwrap();
    write_commented_bool(
        s,
        "# You MUST attribute the author (audit verifies title + author + source are set).",
        "requires_attribution",
        terms.requires_attribution,
    );
    write_commented_bool(
        s,
        "# You MUST reproduce the license text in your distribution.",
        "requires_license_notice",
        terms.requires_license_notice,
    );
    write_commented_bool(
        s,
        "# You MUST offer corresponding source code on distribution (GPL-family). Tracked in the BOM; no audit finding.",
        "requires_source_disclosure",
        terms.requires_source_disclosure,
    );
    writeln!(s, "# Derivatives dimension. One of:").unwrap();
    writeln!(
        s,
        "#   \"disallowed\"  - no derivatives permitted (CC-BY-ND)."
    )
    .unwrap();
    writeln!(
        s,
        "#   \"allowed\"     - derivatives permitted under any terms (MIT, CC-BY, CC0)."
    )
    .unwrap();
    writeln!(
        s,
        "#   \"share-alike\" - derivatives must use the same license (OFL, GPL, CC-BY-SA)."
    )
    .unwrap();
    writeln!(s, "derivatives = \"{}\"", kebab(terms.derivatives)).unwrap();
    write_commented_bool(
        s,
        "# If the asset is modified, you MUST state the modification in credits.",
        "requires_modification_notice",
        terms.requires_modification_notice,
    );
    write_commented_bool(
        s,
        "# You MAY use this commercially.",
        "allows_commercial_use",
        terms.allows_commercial_use,
    );
    write_commented_bool(
        s,
        "# You MAY redistribute (re-host / resell) the asset itself, not just ship it embedded.",
        "allows_redistribution",
        terms.allows_redistribution,
    );
    writeln!(
        s,
        "# The license carries clauses the boolean grid cannot auto-verify (seat limits,"
    )
    .unwrap();
    writeln!(
        s,
        "# territory, field-of-use, ...). FAILs the audit until the license id is listed in"
    )
    .unwrap();
    writeln!(
        s,
        "# `manual_review_acknowledged` in auditah.toml. License-only: not overridable."
    )
    .unwrap();
    writeln!(s, "manual_review = {}", terms.manual_review).unwrap();
    writeln!(s).unwrap();
    writeln!(
        s,
        "# Free-form notes, especially for bespoke/custom licenses. Optional."
    )
    .unwrap();
    writeln!(s, "# notes = \"...\"").unwrap();
}

/// Write a one-line `#` comment followed by a `field = value` boolean.
#[allow(clippy::unwrap_used)] // writeln! to a String is infallible
fn write_commented_bool(s: &mut String, comment: &str, field: &str, value: bool) {
    writeln!(s, "{comment}").unwrap();
    writeln!(s, "{field} = {value}").unwrap();
}

/// Render a `Derivatives` variant to its kebab-case TOML string.
fn kebab(d: crate::model::terms::Derivatives) -> &'static str {
    use crate::model::terms::Derivatives::{Allowed, Disallowed, ShareAlike};
    match d {
        Disallowed => "disallowed",
        Allowed => "allowed",
        ShareAlike => "share-alike",
    }
}

/// Write the license grid template to `<root>/LICENSES/LicenseRef-<name>.toml`.
///
/// Refuses to overwrite an existing file. Returns the path that was written.
///
/// # Errors
///
/// Returns `AddLicenseError` if the target file already exists or writing fails.
pub fn write_license_template(
    services: &Services,
    root: &Path,
    name: &str,
) -> Result<PathBuf, Report<AddLicenseError>> {
    let id = license_ref_id(name);
    let content = render_license_template(&id);
    write_grid(services, root, &id, &content)
}

/// Derive the license id from a written grid path, falling back to `fallback`
/// if the path has no file name. Used by `add-license --custom` to recover the id
/// (which is `LicenseRef-<name>`) for the warning message.
#[must_use]
pub fn grid_id_from_path(path: &Path, fallback: &str) -> String {
    path.file_name().and_then(|f| f.to_str()).map_or_else(
        || fallback.to_string(),
        |f| f.trim_end_matches(".toml").to_string(),
    )
}

/// Compute the on-disk path for a license text: `<root>/LICENSES/<id>.txt`.
#[must_use]
pub fn license_text_path(root: &Path, id: &str) -> PathBuf {
    root.join("LICENSES").join(format!("{id}.txt"))
}

/// Write arbitrary grid content to `<root>/LICENSES/<id>.toml`.
///
/// Refuses to overwrite an existing file.
///
/// # Errors
///
/// Returns `AddLicenseError` if the target file already exists or writing fails.
pub fn write_grid(
    services: &Services,
    root: &Path,
    id: &str,
    content: &str,
) -> Result<PathBuf, Report<AddLicenseError>> {
    let path = license_grid_path(root, id);
    if path.exists() {
        return Err(Report::new(AddLicenseError)
            .attach("license grid already exists; edit it directly or remove it first")
            .attach(path.display().to_string()));
    }
    services
        .fs
        .write(&path, content)
        .change_context(AddLicenseError)
        .attach("failed to write license grid")?;
    Ok(path)
}

/// Write license text to `<root>/LICENSES/<id>.txt`.
///
/// Refuses to overwrite an existing file.
///
/// # Errors
///
/// Returns `AddLicenseError` if the target file already exists or writing fails.
pub fn write_text(
    services: &Services,
    root: &Path,
    id: &str,
    content: &str,
) -> Result<PathBuf, Report<AddLicenseError>> {
    let path = license_text_path(root, id);
    if path.exists() {
        return Err(Report::new(AddLicenseError)
            .attach("license text already exists; remove it first to re-extract")
            .attach(path.display().to_string()));
    }
    services
        .fs
        .write(&path, content)
        .change_context(AddLicenseError)
        .attach("failed to write license text")?;
    Ok(path)
}

/// Provision the requested license into `licenses_dir` if it is absent.
///
/// Matrix: already-present -> skip; well-known -> install text + grid;
/// unknown/custom -> hard error pointing at `add-license --custom`.
///
/// `licenses_dir` is the discovered `LICENSES/` directory; the project root is
/// derived as its parent.
///
/// # Errors
///
/// Returns an error if the license is not present and not a known SPDX id, or
/// if the text/grid writes fail.
pub(crate) fn provision_license(
    services: &Services,
    licenses_dir: &Path,
    license_id: &str,
) -> Result<(), Report<AppError>> {
    // write_grid/license_grid_path take the PROJECT ROOT (they join "LICENSES").
    let project_root = licenses_dir.parent().unwrap_or(Path::new("."));
    let grid_path = license_grid_path(project_root, license_id);
    if grid_path.exists() {
        return Ok(());
    }

    match well_known::resolve(license_id) {
        ResolveResult::NotFound => Err(Report::new(AppError)
            .attach(format!(
                "license {license_id:?} is not in LICENSES/ and is not a known SPDX id"
            ))
            .attach("run `auditah add-license --custom <name>` to create it first")),
        ResolveResult::Found(canonical) => {
            let text = well_known::extract_text(&canonical);
            write_text(services, project_root, &canonical, &text).change_context(AppError)?;
            let grid = well_known::extract_grid(&canonical)
                .unwrap_or_else(|| render_license_template(&canonical));
            write_grid(services, project_root, &canonical, &grid).change_context(AppError)?;
            eprintln!("provisioned {canonical} into LICENSES/");
            Ok(())
        }
    }
}

/// Resolve the copyright year when `--year` is omitted: read the wall
/// clock via `clock` and map epoch seconds to a calendar year. On a broken
/// or pre-epoch clock, fall back to `2026`.
pub(crate) fn year_from_clock(services: &Services) -> u16 {
    services
        .clock
        .now_epoch_secs()
        .map_or(2026, year_from_epoch_secs)
}

/// Map Unix epoch seconds to an approximate calendar year.
///
/// Uses a Julian year (`31_557_600` seconds); year-boundary drift of ±1 day
/// is acceptable for copyright-year attribution.
pub(crate) fn year_from_epoch_secs(secs: u64) -> u16 {
    u16::try_from(secs / 31_557_600 + 1970).unwrap_or(2026)
}

#[cfg(test)]
#[allow(clippy::unwrap_used, clippy::expect_used)]
mod tests {
    use super::*;
    use crate::services::clock::ClockService;
    use crate::test_support::{FakeClock, ServicesTestBuilder};
    use std::sync::Arc;
    use temptree::temptree;

    #[test]
    fn year_from_epoch_secs_maps_a_known_past_year() {
        // Given epoch seconds for 2019-01-01.
        let secs = 1_546_322_400;

        // When mapping to a year.
        let year = year_from_epoch_secs(secs);

        // Then the year is 2019.
        assert_eq!(year, 2019);
    }

    #[test]
    fn year_from_epoch_secs_maps_a_current_era_year() {
        // Given epoch seconds for 2025-01-01.
        let secs = 1_735_689_600;

        // When mapping to a year.
        let year = year_from_epoch_secs(secs);

        // Then the year is 2025.
        assert_eq!(year, 2025);
    }

    #[test]
    fn year_from_epoch_secs_maps_epoch_zero_to_1970() {
        // Given the epoch itself.
        let secs = 0;

        // When mapping to a year.
        let year = year_from_epoch_secs(secs);

        // Then the year is 1970, not 0 (the original off-by-1970 bug).
        assert_eq!(year, 1970);
    }

    #[test]
    fn year_from_epoch_secs_falls_back_when_future_overflows_u16() {
        // Given an absurd future timestamp that would overflow u16.
        let secs = u64::MAX;

        // When mapping to a year.
        let year = year_from_epoch_secs(secs);

        // Then the year is the 2026 fallback rather than panicking.
        assert_eq!(year, 2026);
    }

    #[test]
    fn year_from_clock_maps_a_fixed_instant_to_its_year() {
        // Given a FakeClock pinned to a 2019-01-01 epoch-second instant.
        let services = Services::test()
            .clock(ClockService::new(Arc::new(FakeClock::fixed(1_546_322_400))))
            .build();

        // When resolving the default year from the clock.
        let year = year_from_clock(&services);

        // Then the year is 2019 (not 56 / 0 — the original bug).
        assert_eq!(year, 2019);
    }

    #[test]
    fn year_from_clock_falls_back_to_2026_when_clock_is_broken() {
        // Given a FakeClock that always fails (models a pre-epoch clock).
        let services = Services::test()
            .clock(ClockService::new(Arc::new(FakeClock::broken())))
            .build();

        // When resolving the default year from the broken clock.
        let year = year_from_clock(&services);

        // Then the year is the 2026 fallback rather than erroring or panicking.
        assert_eq!(year, 2026);
    }

    // --- Provisioning matrix (provision_license) ---
    //
    // These exercise the four-cell matrix directly; provision_license is the
    // observable behavior under test. Each test pins one cell.

    fn empty_project_with_licenses() -> (tempfile::TempDir, std::path::PathBuf) {
        let tree = temptree! { LICENSES: {} };
        let root = tree.path().to_path_buf();
        (tree, root)
    }

    #[test]
    fn provision_skips_when_license_grid_already_present() {
        // Given a project whose LICENSES/ already has MIT.toml.
        let (_tree, root) = empty_project_with_licenses();
        std::fs::write(
            root.join("LICENSES/MIT.toml"),
            "id = \"MIT\"\nname = \"handwritten\"\nurl = \"https://x\"\n[terms]\nrequires_attribution = false\nrequires_license_notice = true\nrequires_source_disclosure = false\nderivatives = \"allowed\"\nrequires_modification_notice = false\nallows_commercial_use = true\nallows_redistribution = true\nmanual_review = false\n",
        )
        .unwrap();
        let svc = ServicesTestBuilder::load_from_disk(&root).unwrap().build();
        let original = std::fs::read_to_string(root.join("LICENSES/MIT.toml")).unwrap();

        // When provisioning MIT.
        provision_license(&svc, &root.join("LICENSES"), "MIT").unwrap();

        // Then the grid is untouched (not overwritten) and no .txt appeared.
        let after = std::fs::read_to_string(root.join("LICENSES/MIT.toml")).unwrap();
        assert_eq!(original, after, "existing grid must not be overwritten");
        assert!(!root.join("LICENSES/MIT.txt").exists());
    }

    #[test]
    fn provision_installs_text_and_grid_for_well_known_id() {
        // Given a project whose LICENSES/ is empty.
        let (_tree, root) = empty_project_with_licenses();
        let svc = ServicesTestBuilder::load_from_disk(&root).unwrap().build();

        // When provisioning MIT (well-known, absent).
        provision_license(&svc, &root.join("LICENSES"), "MIT").unwrap();

        // Then both MIT.txt and MIT.toml are written to the discovered LICENSES/.
        assert!(
            license_text_path(&root, "MIT").exists(),
            "MIT.txt must be written"
        );
        assert!(
            license_grid_path(&root, "MIT").exists(),
            "MIT.toml must be written"
        );
        let grid = std::fs::read_to_string(license_grid_path(&root, "MIT")).unwrap();
        assert!(
            grid.contains("id = \"MIT\""),
            "grid must carry the canonical id"
        );
    }

    #[test]
    fn provision_hard_errors_when_id_is_unknown_and_absent() {
        // Given a project whose LICENSES/ has no StudioEULA grid.
        let (_tree, root) = empty_project_with_licenses();
        let svc = ServicesTestBuilder::load_from_disk(&root).unwrap().build();

        // When provisioning an unknown id.
        let result = provision_license(&svc, &root.join("LICENSES"), "StudioEULA");

        // Then it hard-errors with a pointer at `add-license --custom`.
        let report = result.expect_err("unknown id must error");
        let rendered = format!("{report:?}");
        assert!(
            rendered.contains("--custom"),
            "error must mention add-license --custom: {rendered}"
        );
        assert!(
            rendered.contains("StudioEULA"),
            "error must name the offending id: {rendered}"
        );
    }
}
